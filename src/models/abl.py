def t_NUMBER(t):
    r'\d+(\.\d*)?'  # Matches integers and floating-point numbers
    t.value = float(t.value)    
    return t

def t_STRING(t):
    r'\"[^"]*\"'

    t.value = t.value[1:-1] # Remove the enclosing double quotes
    return t


